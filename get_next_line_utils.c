#include "get_next_line.h"

int	ft_strlen(char *str)
{
	size_t	i;
	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

char	*ft_strjoin(char *s1, char *s2)
{
	char	*result;
	size_t	i;
	size_t	j;

	result = malloc(ft_strlen(s1) + ft_strlen(s2) + 1);
	if (!result)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		result[i + j] = s2[j];
		j++;
	}
	result[i + j] = '\0';
	return (result);
}

char	*ft_strchr(const char *s, char c)
{
	size_t	i;

	while (s[i])
	{
		if (s[i] == c)
		{
			return (char *) s[i];
		}
		i++;
	}
	return (NULL);
}



